/// Returns project banner as a text art with a version tag
///
/// # Banner
///
/// ```text
///            _
///           | |
///  ___ _   _| |__  ___  ___ __ _ _ __
/// / __| | | | '_ \/ __|/ __/ _` | '_ \
/// \__ \ |_| | |_) \__ \ (_| (_| | | | |
/// |___/\__,_|_.__/|___/\___\__,_|_| |_|
///
/// ```
pub fn banner() -> String {
    format!(
        r"
            _
           | |
  ___ _   _| |__  ___  ___ __ _ _ __
 / __| | | | '_ \/ __|/ __/ _` | '_ \
 \__ \ |_| | |_) \__ \ (_| (_| | | | |
 |___/\__,_|_.__/|___/\___\__,_|_| |_|

                                v{}
                                      ",
        env!("CARGO_PKG_VERSION")
    )
}
